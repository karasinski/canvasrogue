var blanks = 64;
var finalMapWidth = mapWidth + blanks;
var finalMapHeight = mapHeight + blanks;

var finalMap = [];
for (var i = 0; i < finalMapWidth; i++) { 
	finalMap[i] = []; 
	for (var j = 0; j < finalMapHeight; j++) { 
    finalMap[i][j] = 9; 
  } 
} 

for (var i = (blanks/4)-1; i < finalMapWidth-(blanks/4); i++) { 
	for (var j = (blanks/4)-1; j < finalMapHeight-(blanks/4); j++) { 
    finalMap[i][j] = 1; 
  } 
} 

for (var y = 0; y < mapHeight; y++) {
	for (var x = 0; x < mapWidth; x++) {
		//console.log('x: ' + x + ' y: ' + y);
		finalMap[y+blanks/2][x+blanks/2] = map[y][x];
	}
}
