function mapGen(map) {
	//prevent map from edging/encase in rock
	mapWidth = map[0].length;
	mapHeight = map.length;
	//console.log('mapWidth ' + mapWidth + ', mapHeight ' + mapHeight);
	
	blanks = 64;
	finalMapWidth = mapWidth + blanks;
	finalMapHeight = mapHeight + blanks;
	
	//var finalMap = [];
	for (var i = 0; i < finalMapWidth; i++) { 
		finalMap[i] = []; 
		for (var j = 0; j < finalMapHeight; j++) { 
			finalMap[i][j] = 9; 
		} 
	} 
	
	for (var i = (blanks/4)-1; i < finalMapWidth-(blanks/4); i++) { 
		for (var j = (blanks/4)-1; j < finalMapHeight-(blanks/4); j++) { 
			finalMap[i][j] = 1; 
		} 
	} 
	
	for (var y = 0; y < mapHeight; y++) {
		for (var x = 0; x < mapWidth; x++) {
			//console.log('x: ' + x + ' y: ' + y);
			finalMap[y+blanks/2][x+blanks/2] = map[y][x];
		}
	}
	//console.log(finalMap);
	
	//create fow map
	//var fowMap = [];
	for (var i = 0; i < finalMapWidth; i++) { 
		fowMap[i] = []; 
		for (var j = 0; j < finalMapHeight; j++) { 
			fowMap[i][j] = 0; 
		} 
	}
	
	//console.log(fowMap);
	
	
	/*--------------------------------------------
		We are generating random numbers for assigning 
		grid graphics to the ground and the walls. Neat!
	--------------------------------------------*/
	//create rand wall map
	//var randMap = [];
	for (var i = 0; i < finalMapWidth; i++) { 
		randMap[i] = []; 
		for (var j = 0; j < finalMapHeight; j++) { 
			var randomIndex = Math.floor((Math.random()*7));
			randMap[i][j] = randomIndex; 
		} 
	}
	
	//create rand ground map
	//var randgMap = [];
	for (var i = 0; i < finalMapWidth; i++) { 
		randgMap[i] = []; 
		for (var j = 0; j < finalMapHeight; j++) { 
			var randomIndex = Math.floor((Math.random()*6));
			randgMap[i][j] = randomIndex; 
		} 
	}
	
};

